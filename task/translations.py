def t(key, lang='id'):
    """
    Translate a key to the requested language.

    Args:
        key: Translation key (e.g. 'nav.home')
        lang: Language code, defaults to 'id'

    Returns:
        Translated string, falling back to Indonesian, then to the key itself.
    """
    return T.get(lang, T['id']).get(key, T['id'].get(key, key))
